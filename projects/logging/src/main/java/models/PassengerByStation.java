public class PassengerByStation {
    private float station_id;
    private String station_name;
    private float passenger_count;

    // Getter Methods

    public float getStation_id() {
        return station_id;
    }

    public String getStation_name() {
        return station_name;
    }

    public float getPassenger_count() {
        return passenger_count;
    }

    // Setter Methods

    public void setStation_id(float station_id) {
        this.station_id = station_id;
    }

    public void setStation_name(String station_name) {
        this.station_name = station_name;
    }

    public void setPassenger_count(float passenger_count) {
        this.passenger_count = passenger_count;
    }
}